    private void send_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_btnActionPerformed
        try {
            String selectedOwner = (String) ownersList_cbx.getSelectedItem();
            String ownersCpf = main.Main.menuFuncs.extractIdentifier(selectedOwner);
            Owner owner = main.Main.menuFuncs.getOwner(ownersCpf);        

            String name = name_txt.getText();
            int age = Integer.parseInt(age_txt.getText());
            String animalSpecies = animalSpecies_txt.getText();
            String details = details_txt.getText();

            owner.addNewAnimal(name, age, animalSpecies, details);
            JOptionPane.showMessageDialog(null, "Animal cadastrado com sucesso!");
            Model.MenuFuncs_DAO.changeScreen(this, new MainMenu_GUI());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro!\nCÃ³digo do erro: " + e);
        }
    }//GEN-LAST:event_send_btnActionPerformed

