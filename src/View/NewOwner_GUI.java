    private void send_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_btnActionPerformed
        try {
            String name = name_txt.getText();
            String cpf = cpf_txt.getText();
            String phoneNumber = phoneNumber_txt.getText();

            if (main.Main.menuFuncs.addNewOwner(cpf, name, phoneNumber)) {
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
                Model.MenuFuncs_DAO.changeScreen(this, new NewAnimal_GUI());
            } else {
                JOptionPane.showMessageDialog(null, "CPF " + cpf_txt.getText() + " ja esta cadastrado.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro!\nCÃ³digo do erro: " + e);
        }
    }//GEN-LAST:event_send_btnActionPerformed

