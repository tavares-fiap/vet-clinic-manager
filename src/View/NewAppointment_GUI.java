    private void send_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send_btnActionPerformed
        try {
            String appointmentDate = appointmentDate_txt.getText();
            String appointmentReason = appointmentReason_txt.getText();
            String isTreated = (String) isTreated_cbx.getSelectedItem();
            String selectedVet = (String) vetsList_cbx.getSelectedItem();
            String vetsUsername = main.Main.menuFuncs.extractIdentifier(selectedVet);    
            User vet = main.Main.menuFuncs.getVet(vetsUsername);

            if (main.Main.menuFuncs.addNewAppointment(main.Main.menuFuncs.getSearchedAnimal(), appointmentDate, appointmentReason, isTreated, vet)) {
                JOptionPane.showMessageDialog(null, "Consulta cadastrada com sucesso!");
                main.Main.menuFuncs.setSearchedAnimal(null);
                Model.MenuFuncs_DAO.changeScreen(this, new MainMenu_GUI());
            } else {
                JOptionPane.showMessageDialog(null, "Algo deu errado.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro!\nCÃ³digo do erro: " + e);
        }
    }//GEN-LAST:event_send_btnActionPerformed

